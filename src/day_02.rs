fn dive(input: Vec<String>) -> u32 {
  let mut horiz = 0;
  let mut depth = 0;
  let mut aim = 0;

  for movement in input {
    let foo = movement.split(" ").collect::<Vec<&str>>();
    let direction = foo[0];
    let distance: u32 = foo[1].parse().unwrap();

    match (direction, distance) {
      ("forward", d) => {
        horiz += d;
        depth += aim * d;
      }
      ("down", d) => {
        aim += d;
        depth += d;
      }
      ("up", d) => {
        aim -= d;
        depth -= d;
      }
      (_, _) => panic!("Unknown direction"),
    }
  }

  horiz * depth
}

